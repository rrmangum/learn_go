package main

import "fmt"

func main() {
	name := "Ryan"
	fmt.Printf("The programmer's name is: %s\t And the type is: %T\n", name, name)

	age := 31
	fmt.Printf("The programmer's age is: %d\t And the type is: %T\n", age, age)

	moneyMade := 3300.21
	fmt.Printf("The programmer made: %g\t And the type is: %T\n", moneyMade, moneyMade)

}
